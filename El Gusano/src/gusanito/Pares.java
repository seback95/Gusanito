package gusanito;

public class Pares {
	private int nodo;
	private int tiempo;
	public Pares(int n,int t)
	{
		nodo=n;
		tiempo=t;
	}
	public int getNodo() {
		return nodo;
	}
	public void setNodo(int nodo) {
		this.nodo = nodo;
	}
	public int getTiempo() {
		return tiempo;
	}
	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}

}
